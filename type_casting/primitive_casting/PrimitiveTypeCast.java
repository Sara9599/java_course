package com.xworkz.xworkz_java.type_casting.primitive_casting;

public class PrimitiveTypeCast {
	public static void main(String[] args) {
		//implicit
			byte b=10;
			short s=b;
			System.out.println(s);
		//explicit
			double d=1000.101800;
			float f=(float)d;
			System.out.println(f);
	
}
}
