package com.xworkz.xworkz_java.Encapsulation;

public class WomenClothes{
	private String color;
	private String name;
	private String type;
	private int size;
	private int Cost;
	WomenClothes(){
		
	}
	public String getColor() {
		return color;
	}
	public String getName() {
		return name;
	}
	public String getType() {
		return type;
	}
	public int getSize() {
		return size;
	}
	public int getCost() {
		return Cost;
	}
	
}
